"use client";

import Link from "next/link";
import { useRouter } from "next/navigation";
import { useEffect, useMemo, useState } from "react";
import { Button } from "@/components/ui/button";
import AppShellHeader from "@/components/AppShellHeader";
import { Card } from "@/components/ui/card";
import { getDisplayCompanyName } from "@/lib/job";
import { createClient } from "../../../lib/supabase/client";

type GeneratedRow = {
  id: string;
  job_url: string;
  company_name: string | null;
  download_url: string | null;
  created_at: string | null;
};

type CompanyGroup = {
  company: string;
  rows: GeneratedRow[];
};

function formatCreatedAt(value: string | null) {
  if (!value) {
    return "Unknown date";
  }
  const date = new Date(value);
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
    timeZone: "UTC",
  });
}

export default function GeneratedPage() {
  const router = useRouter();
  const supabase = useMemo(() => createClient(), []);
  const [isCheckingAuth, setIsCheckingAuth] = useState(true);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const [rows, setRows] = useState<GeneratedRow[]>([]);

  useEffect(() => {
    const guard = async () => {
      const {
        data: { session },
      } = await supabase.auth.getSession();

      if (!session?.user) {
        router.replace("/login");
        return;
      }

      setIsCheckingAuth(false);
      setLoading(true);

      await fetch("/api/generations/enrich-company", {
        method: "POST",
      }).catch(() => null);

      const { data, error: queryError } = await supabase
        .from("generations")
        .select("*")
        .eq("user_id", session.user.id)
        .eq("status", "completed")
        .order("created_at", { ascending: false });

      if (queryError) {
        setError("Failed to load generated resumes.");
        setRows([]);
      } else {
        setError(null);
        setRows((data ?? []) as GeneratedRow[]);
      }

      setLoading(false);
    };

    const { data: authListener } = supabase.auth.onAuthStateChange((_event, session) => {
      if (!session?.user) {
        router.replace("/login");
      }
    });

    void guard();
    return () => {
      authListener.subscription.unsubscribe();
    };
  }, [router, supabase]);

  const grouped = useMemo<CompanyGroup[]>(() => {
    const map = new Map<string, GeneratedRow[]>();
    rows.forEach((row) => {
      const company = getDisplayCompanyName(row.company_name, row.job_url);
      map.set(company, [...(map.get(company) ?? []), row]);
    });

    return Array.from(map.entries())
      .map(([company, companyRows]) => ({ company, rows: companyRows }))
      .sort((a, b) => b.rows.length - a.rows.length);
  }, [rows]);

  if (isCheckingAuth) {
    return <main className="app-shell-bg min-h-screen" />;
  }

  return (
    <main className="app-shell-bg min-h-screen px-3 pb-8 pt-44 sm:px-6 md:pt-32">
      <AppShellHeader subtitle="Generated Resume Portfolio" />
      <div className="mx-auto w-full max-w-6xl space-y-6">
        <section className="grid gap-4 sm:grid-cols-3">
          <Card className="p-5">
            <p className="text-xs uppercase tracking-[0.08em] text-[var(--text-muted)]">Completed Resumes</p>
            <p className="mt-2 text-3xl font-semibold text-[var(--text-primary)]">{rows.length}</p>
          </Card>
          <Card className="p-5">
            <p className="text-xs uppercase tracking-[0.08em] text-[var(--text-muted)]">Companies Covered</p>
            <p className="mt-2 text-3xl font-semibold text-[var(--text-primary)]">{grouped.length}</p>
          </Card>
          <Card className="p-5">
            <p className="text-xs uppercase tracking-[0.08em] text-[var(--text-muted)]">Latest Output</p>
            <p className="mt-2 text-sm font-medium text-[var(--text-primary)]">
              {rows[0]?.created_at ? formatCreatedAt(rows[0].created_at) : "No data"}
            </p>
          </Card>
        </section>

        <Card className="p-6">
          <h1 className="text-2xl font-semibold text-[var(--text-primary)] sm:text-3xl">Generated by company</h1>
          <p className="mt-2 text-sm text-[var(--text-secondary)]">
            Unlike History, this page is organized by company so you can review completed resumes portfolio-style.
          </p>
        </Card>

        <Card className="p-6">
          {loading ? <p className="text-sm text-[var(--text-secondary)]">Loading generated resumes...</p> : null}
          {error ? <p className="text-sm text-[var(--danger)]">{error}</p> : null}
          {!loading && !error && grouped.length === 0 ? (
            <p className="text-sm text-[var(--text-secondary)]">No completed resumes yet.</p>
          ) : null}

          <div className="space-y-5">
            {grouped.map((group) => (
              <div key={group.company} className="rounded-xl border border-[var(--border-glass)] bg-[rgba(8,12,23,0.72)] p-4">
                <div className="mb-3 flex flex-wrap items-center justify-between gap-2">
                  <div>
                    <p className="text-[11px] font-semibold uppercase tracking-[0.08em] text-[var(--accent-2)]">Company</p>
                    <p className="text-lg font-semibold text-[var(--text-primary)]">{group.company}</p>
                  </div>
                  <p className="rounded-md border border-[var(--border-glass)] px-2 py-1 text-xs text-[var(--text-secondary)]">
                    {group.rows.length} resume{group.rows.length > 1 ? "s" : ""}
                  </p>
                </div>

                <div className="space-y-3">
                  {group.rows.map((row) => (
                    <div key={row.id} className="glass-card flex flex-col gap-3 rounded-lg p-3 sm:flex-row sm:items-center sm:justify-between">
                      <div className="min-w-0">
                        <p className="truncate text-sm text-[var(--text-primary)]" title={row.job_url}>{row.job_url}</p>
                        <p className="mt-1 text-xs text-[var(--text-muted)]">{formatCreatedAt(row.created_at)}</p>
                      </div>
                      <div className="flex items-center gap-2">
                        <Link href={`/resumes/${row.id}`}>
                          <Button size="sm" variant="outline">Open Page</Button>
                        </Link>
                        {row.download_url ? (
                          <Button size="sm" onClick={() => window.open(row.download_url!, "_blank", "noopener,noreferrer")}>
                            Download
                          </Button>
                        ) : null}
                      </div>
                    </div>
                  ))}
                </div>
              </div>
            ))}
          </div>
        </Card>
      </div>
    </main>
  );
}
